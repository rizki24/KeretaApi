    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ("".equals(txtktp.getText()) || "".equals(txtumur.getText()) || "".equals(txtnama.getText()) || "".equals(txtjurusan.getText()) || "".equals(txttgl.getText()) || "".equals(txtwaktu.getText()) || "".equals(txtjenis.getText()) ) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else{
            String JK = "";

            if (rdLAKI.isSelected()) {
                JK = "L";
            }else{
                JK = "P";
            }
            String SQL = "INSERT INTO t_daftar (KTP,Nama,JenisKelamin,Umur,Jurusan,TanggalBerangkat,WaktuBerangkat,JenisKereta)"
            + "VALUE('"+txtktp.getText()+"','"+txtnama.getText()+"','"+JK+"',"
            + "'"+txtumur.getText()+"','"+txtjurusan.getText()+"','"+txttgl.getText()+"','"+txtwaktu.getText()+"','"+txtjenis.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan", "Sukses", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int baris = tb1Data.getSelectedRow();
        if (baris != -1) {
            String KTP = tb1Data.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM t_daftar WHERE KTP='"+KTP+"'";
            int status = KoneksiDB.execute(SQL);
            if (status==1) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus","Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus","Gagal",JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih Dahulu", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

