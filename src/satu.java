    private void selectData() {
        String kolom[] = {"KTP","Nama","JenisKelamin","Umur","Jurusan","TanggalBerangkat","WaktuBerangkat","JenisKereta"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM t_daftar";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while (rs.next()) {
                String KTP = rs.getString(1);
                String Nama = rs.getString(2);
                String JenisKelamin = "";
                if ("L".equals(rs.getString(3))) {
                    JenisKelamin = "Laki-Laki";
                } else {
                    JenisKelamin = "Perempuan";
                }
                String Umur = rs.getString(4);
                String Jurusan = rs.getString(5);
                String TanggalBerangkat = rs.getString(6);
                String WaktuBerangkat = rs.getString(7);
                String JenisKereta = rs.getString(8);
                
                String data[] = {KTP,Nama,JenisKelamin,Umur,Jurusan,TanggalBerangkat,WaktuBerangkat,JenisKereta,};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(satu.class.getName()).log(Level.SEVERE, null, ex);
        }
        tb1Data.setModel(dtm); 
    }
